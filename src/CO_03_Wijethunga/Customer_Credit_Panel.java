    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jButton4.getText() == "Save") {
            if (jTextField1.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Customer ID !");
                jTextField1.grabFocus();
            } else if (jTextField2.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Credit Limit !");
                jTextField2.grabFocus();
            } else if (jDateChooser1.getDate().toString().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Start Date !");
            } else if (jDateChooser2.getDate().toString().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter End Date !");
            } else if (jTextField8.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Installments !");
                jTextField8.grabFocus();
            } else {

                Date d1 = jDateChooser1.getDate();
                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                String dd = df.format(d1);

                Date d2 = jDateChooser2.getDate();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String ddd = sdf.format(d2);

                try {
                    DB.MyDB.iud("INSERT INTO cutomer_credit_details VALUES ('" + jTextField3.getText() + "','" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + dd + "','" + ddd + "','" + jTextField8.getText() + "','" + 1 + "','" + 20000.00 + "','" + 20000.00 + "')");
                    JOptionPane.showMessageDialog(this, "Successfully Saved..");

                    jTextField3.setText("");
                    jTextField1.setText("");
                    jLabel1.setText("Customer Name");
                    jTextField2.setText("");
                    jTextField8.setText("");

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        } else if (jButton4.getText() == "Update") {

        }

    }//GEN-LAST:event_jButton4ActionPerformed

