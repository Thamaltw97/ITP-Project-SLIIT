  private void GenerateID() {
        try {
            ResultSet rs = DB.MyDB.search("SELECT Max(idCutomer_credit_details) FROM cutomer_credit_details");
            if (rs.next()) {
                jTextField3.setText(Integer.parseInt(rs.getString("max(idCutomer_credit_details)")) + 1 + "");
                jButton4.setText("Save");
            } else {
                jButton4.setText("Save");
                jTextField3.setText("1");
            }
        } catch (Exception e) {
            jTextField3.setText("1");
        }
    }
