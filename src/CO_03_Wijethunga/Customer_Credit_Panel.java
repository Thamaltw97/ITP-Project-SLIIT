    private void jTextField3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField3ActionPerformed

        try {
            String query = "";
            query = "SELECT * FROM cutomer_credit_details WHERE idCutomer_credit_details LIKE '" + jTextField3.getText() + "%'";
            ResultSet rs = DB.MyDB.search(query);
            if (rs.next()) {
                jTextField1.setText(rs.getString("customer_idcustomer"));
                jTextField2.setText(rs.getString("credit_limit"));
                jDateChooser1.setDate(java.sql.Date.valueOf(rs.getString("start_date")));
                jDateChooser2.setDate(java.sql.Date.valueOf(rs.getString("end_date")));
                jTextField8.setText(rs.getString("installments"));

                jButton4.setText("Update");
            } else {
                jButton4.setText("Save");

            }

            ResultSet rs1 = DB.MyDB.search("select * from customer where idcustomer='" + jTextField1.getText() + "'");
            if (rs1.next()) {
                jLabel1.setText(rs1.getString("name"));

            } else {
                jLabel1.setText("Customer Name");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        jTextField1.grabFocus();
    }//GEN-LAST:event_jTextField3ActionPerformed

