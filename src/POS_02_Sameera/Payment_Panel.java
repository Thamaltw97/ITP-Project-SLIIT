    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        Double gross_Amount = 0.0;
        Double dis = 0.0;
        Double dis_price = 0.0;

        if (jTextField2.getText().trim().isEmpty()) {
            jTextField5.grabFocus();
        } else {
            gross_Amount = Double.parseDouble(jTextField1.getText());
            dis = Double.parseDouble(jTextField2.getText());
            dis_price = (gross_Amount / 100) * dis;
            jTextField5.setText(dis_price + "");
            Double final_amount = 0.0;
            final_amount = gross_Amount - dis_price;
            jTextField6.setText(final_amount + "");
            jTextField5.grabFocus();
        }
    }//GEN-LAST:event_jTextField2ActionPerformed

