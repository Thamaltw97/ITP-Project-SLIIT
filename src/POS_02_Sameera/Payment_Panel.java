    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField7.getText().trim().isEmpty() && jTextField8.getText().trim().isEmpty() && jTextField9.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter payment method !");
            jTextField7.grabFocus();
        } else if (jTextField6.getText().trim().isEmpty() || jTextField6.getText().equals("0.0")) {
            JOptionPane.showMessageDialog(this, "Please enter Amount !");
            jTextField6.grabFocus();
        } else {
            Date d = Invoice_panel.jDateChooser2.getDate();
            SimpleDateFormat sf = new SimpleDateFormat("yyyy-MM-dd");
            String d1 = sf.format(d);
            String type = "";
            String amount = "";

            if (jLabel19.getForeground().equals(Color.red)) {
                type = "Cash";
                amount = jTextField7.getText();
            } else if (jLabel20.getForeground().equals(Color.red)) {
                type = "Card";
                amount = jTextField8.getText();
            } else if (jLabel18.getForeground().equals(Color.red)) {
                type = "Debit";
                amount = jTextField9.getText();
            }
            DefaultTableModel df = (DefaultTableModel) Invoice_panel.jTable1.getModel();
            //idinvoice, date, user_username, total_amount, discount_type, discount, discount_amount, bill_amount, payment_type, payment, balance, banks_idbanks, status, return_note_idreturn_note
            int kk = JOptionPane.showConfirmDialog(this, "Are you sure to save this ?", "WARNING", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (kk == 0) {
                try {
                    DB.MyDB.iud("insert into invoice values('" + Invoice_panel.jTextField2.getText() + "','" + d1 + "','" + Invoice_panel.jLabel3.getText() + "','" + jTextField1.getText() + "','0','" + jTextField2.getText() + "','" + jTextField5.getText() + "','" + jTextField6.getText() + "','" + type + "','" + amount + "','" + jLabel22.getText() + "','1','1','1')");
                    for (int i = 0; i < Invoice_panel.jTable1.getRowCount(); i++) {
                        DB.MyDB.iud("insert into invoice_has_item values('" + df.getValueAt(i, 0) + "','" + df.getValueAt(i, 1) + "','" + df.getValueAt(i, 6) + "','" + df.getValueAt(i, 4) + "','" + df.getValueAt(i, 7) + "')");
                        ResultSet rs = DB.MyDB.search("select * from item where iditem='" + df.getValueAt(i, 1) + "'");
                        if (rs.next()) {
                            String a = rs.getString("qty");
                            String b = df.getValueAt(i, 6).toString();
                            Double qty = Double.parseDouble(a);
                            Double qty1 = Double.parseDouble(b);
                            Double new_qty = qty - qty1;

                            DB.MyDB.iud("Update item set qty='" + new_qty + "' where iditem='" + df.getValueAt(i, 1) + "'");
                        }
                    }
                    JOptionPane.showMessageDialog(this, "Successfully Doing Business !");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField7.setText("");
                    jTextField8.setText("");
                    jTextField9.setText("");
                    jTextField10.setText("");
                    jLabel22.setText("0.0");
                    jLabel25.setText("0");
                    Invoice_panel.generateID();
                    Invoice_panel.amount.setText("0");
                    Invoice_panel.num_items.setText("0");
                    df.setRowCount(0);

//                    this.dispose();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


