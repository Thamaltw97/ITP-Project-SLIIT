public void load() {

        jButton2.setText("Save");
        jTextField7.setText("");
        jLabel8.setText("");

        try {

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            ResultSet rs1 = DB.MyDB.search("select * from sub_category");
            while (rs1.next()) {

                Vector v = new Vector();
                v.add(rs1.getString("id"));
                v.add(rs1.getString("description"));
                df.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        max();
    }

    public void max() {

        try {
            ResultSet rs = DB.MyDB.search("select max(id) from sub_category");
            if (rs.next()) {

                jLabel8.setText(Integer.parseInt(rs.getString("max(id)")) + 1 + "");
                jTextField7.setText("");
            } else {

                jLabel8.setText("1");
                jTextField7.setText("");

            }
            jButton2.setText("Save");
        } catch (Exception e) {
            jLabel8.setText("1");
            jTextField7.setText("");
        }
    }
