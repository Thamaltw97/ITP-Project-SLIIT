    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (user1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter User Name !");
            user1.grabFocus();
        } else if (pass.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter Password !");
            pass.grabFocus();
        } else {
            try {

                if (JOptionPane.showConfirmDialog(this, "Are you sure to add this user?") == 0) {

                    if (pass.getText().equals(c_pass.getText())) {

                        try {

                            DB.MyDB.getDBCon().createStatement().executeUpdate("Insert into user values('" + user1.getText() + "','" + pass.getText() + "','" + jComboBox1.getSelectedItem() + "')");
                            JOptionPane.showMessageDialog(rootPane, "Successfully created !");
                            user1.setText("");
                            c_pass.setText("");
                            pass.setText("");
                            userpanal.load();

                        } catch (Exception e) {
                            e.printStackTrace();
                        }

                    } else {
                        pass.setText("");
                        c_pass.setText("");
                        pass.grabFocus();
                        JOptionPane.showMessageDialog(rootPane, "Passwords not match");
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

