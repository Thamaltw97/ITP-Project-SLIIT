    private void empIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empIDActionPerformed

        try {
            ResultSet rs = DB.MyDB.search("select * from employee where idEmployee = '" + empID.getText() + "' ");
            if (rs.next()) {

                name.setText(rs.getString("name"));
                l1.setText(rs.getString("address_1"));
                l2.setText(rs.getString("address_2"));
                l3.setText(rs.getString("address_3"));
                city.setText(rs.getString("city"));
                cn.setText(rs.getString("contact_1"));
                home.setText(rs.getString("contact_2"));
                nic.setText(rs.getString("nic"));

                if (rs.getString("gender").equals("Male")) {

                    male.setSelected(true);
                } else {

                    female.setSelected(true);
                }
                if (rs.getString("status").equals("1")) {
                    act.setSelected(true);
                } else {
                    inact.setSelected(true);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        name.grabFocus();
    }//GEN-LAST:event_empIDActionPerformed

