     private void GenerateID() {
//get the employee ID
        try {
            ResultSet rs = DB.MyDB.search("select max(idEmployee) from employee ");
            if (rs.next()) {
                empID.setText(Integer.parseInt(rs.getString("max(idEmployee)")) + 1 + "");
            }

        } catch (Exception e) {
            empID.setText("1");
        }
    }
