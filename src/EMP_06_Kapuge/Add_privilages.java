    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed

        DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
        int x = jTable1.getSelectedRow();

        int k = JOptionPane.showConfirmDialog(this, "Are you sure you want to update ?", "WARNNING!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (k == 0) {
            try {
                DB.MyDB.iud("update privilleges set action = '"+jTextField1.getText()+"',description='"+jTextArea1.getText()+"' where idPrivilleges='"+df.getValueAt(x, 0)+"'");
                JOptionPane.showMessageDialog(this, "Successfully Updated !");
            } catch (Exception e) {
                e.printStackTrace();
            }
            jTextField1.setText("");
            jTextArea1.setText("");
            jTextField1.grabFocus();
            loadTable();
        }
    }//GEN-LAST:event_update_btnActionPerformed

