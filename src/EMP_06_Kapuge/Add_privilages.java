 private void loadTable() {
        
        DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
        try {
            ResultSet rs = DB.MyDB.search("select * from privilleges");
            df.setRowCount(0);
            
            while (rs.next()) {                
                Vector v = new Vector();
                
                v.add(rs.getString("idPrivilleges"));
                v.add(rs.getString("action"));
                v.add(rs.getString("description"));
                df.addRow(v);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
