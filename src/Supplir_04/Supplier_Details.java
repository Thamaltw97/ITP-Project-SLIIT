    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //        id, id_letter, id_no, name, address_1, address_2, address_3, city, tp1, tp2, status
        //  save button

        if (s_name.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Supplier Name !");
            s_name.grabFocus();
        } else if (s_id.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Supplier ID !");
            s_id.grabFocus();
        } else if (a1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address 1 !");
            a1.grabFocus();
        } else if (a2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address 2 !");
            a2.grabFocus();
        } else if (a3.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Address 3 !");
            a3.grabFocus();
        } else if (c1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Contact Number !");
            c1.grabFocus();
        } else {
            String status = "";
            if (jCheckBox1.isSelected()) {
                status = "1";
            } else if (jCheckBox2.isSelected()) {
                status = "0";
            }

            int r = JOptionPane.showConfirmDialog(this, "Do you want to save this data ?", "WARNING !", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (r == 0) {

                try {
                    DB.MyDB.iud("INSERT INTO supplier VALUES ('" + s_id.getText() + "' ,'" + s_name.getText() + "', '" + a1.getText() + "', '" + a2.getText() + "', '" + a3.getText() + "', '" + city.getText() + "', '" + c1.getText() + "', '" + c2.getText() + "', '" + status + "')");
                    JOptionPane.showMessageDialog(this, "Successfully Saved..");
                    s_name.setText("");
                    s_id.setText("");
                    a1.setText("");
                    a2.setText("");
                    a3.setText("");
                    city.setText("");
                    c1.setText("");
                    c2.setText("");

                    jCheckBox1.setSelected(true);
                    generateID();
                    s_id.grabFocus();

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

