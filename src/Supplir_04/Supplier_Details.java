private void generateID() {

        try {
            ResultSet rs = DB.MyDB.search("SELECT Max(id) FROM supplier");
            if (rs.next()) {
                s_id.setText(Integer.parseInt(rs.getString("max(id)")) + 1 + "");
            } else {
                s_id.setText("1");
            }
        } catch (Exception e) {
            s_id.setText("1");
        }
    }

    public static void search() {
//        id, id_letter, id_no, name, address_1, address_2, address_3, city, tp1, tp2, status
        try {
            ResultSet rs = DB.MyDB.search("select * from supplier where id='" + s_id.getText() + "'");
            if (rs.next()) {
                s_name.setText(rs.getString("name"));
                a1.setText(rs.getString("address_1"));
                a2.setText(rs.getString("address_2"));
                a3.setText(rs.getString("address_3"));
                city.setText(rs.getString("city"));
                c1.setText(rs.getString("tp1"));
                c2.setText(rs.getString("tp2"));
                if (rs.getString("status").equals("1")) {
                    jCheckBox1.setSelected(true);
                } else {
                    jCheckBox2.setSelected(true);

                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

